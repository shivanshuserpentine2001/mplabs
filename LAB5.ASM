.MODEL SMALL
DISPLAY MACRO MSG
LEA DX,MSG
MOV AH,09H
INT 21H
ENDM
.DATA
LIST DB 02H,01H,34H,0F4H,09H,05H
NUMBER EQU $-LIST
MSG1 DB 0DH,0AH,"1>>SORT IN ASC$"
MSG2 DB 0DH,0AH,"2>>SORT IN DES$"
MSG3 DB 0DH,0AH,"3>>EXIT$"
MSG4 DB 0DH,0AH,"4>>ENTER A CHOICE$"
MSG5 DB 0DH,0AH,"5>>INVALID$"
.CODE
START:MOV AX,@DATA
MOV DS,AX
LEA SI,LIST
MOV CH,NUMBER-1
DISPLAY MSG1
DISPLAY MSG2
DISPLAY MSG3
DIAPLAY MSG4
MOV AH,01H
INT 21H
SUB AL,30H
CMP AL,01H
JE ASCORT
CMP AL,02H
JE DESSORT
CMP AL,03H
JE FINAL
DISPLAY MSG5
JMP FINAL
ASCORT:MOV BL,00H
AGAIN:MOV SI,OFFSET LIST
MOV CL,00H
MOV BH,CH
SUB BH,BL
NPASS:CMP CL, BH
JNC NEXT
MOV AL,[SI]
MOV BP,01H
CMP AL,DS:[BP][SI]
JC_NOPE
XCHG AL,[SI+1]
XCHG [SI],AL
_NOPE:INC CL
INC SII
JMP NPASS
NEXT:INC BL
CMP BL,CH
JC AGAIN
JMP FINAL
DESSORT:MOV BL,00H
AGAIN1:MOV SI,OFFSET LIST
MOV CL,00H
MOV BH,CH
SUB BH,BL
NPASS1: CMP CL,BH
JNC NEXT
MOV AL,[SI]
MOV BP,01H
CMP AL,DS:[BP][SI]
JNC_NOPE1
XCHG AL,[SI+1]
XCHNG [SI],AL
_NOPE1:
INC SI
JMP NPASS1
NEXT1: INC BL
CMP BL,CH
JC AGAIN1
FINAL:MOV AH,4CH
INT 21H
END START

